class Role(object):
    def __init__(self):
        self.nickname = ""    # 昵称
        self.strength = 0   # 力量值
        self.agility = 0    # 敏捷度
        self.power = 0      # 体力
        self.intelligence = 0  # 智力
        self.wisdom = 0      # 智慧
        self.race = ""       # 种族
        self.profession = ""  # 职业
        self.sex = ""         # 性别
        self.life = 0         # 生命值
        self.magic = 0        # 魔法值


